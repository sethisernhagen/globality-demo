exports.getItemsRandom = function(numItems) {

	var output = [],
		i;

	for (i = 0; i < numItems; i++) {
		output.push(items[Math.floor(Math.random() * items.length)]);
	}

	return output;
};

exports.getItemsPage = function(page, numItems) {

	//find page indexes for given page
	var startIndex = (page - 1) * numItems,
		endIndex = startIndex + (numItems - 1);

	return items.slice(startIndex, endIndex);

};